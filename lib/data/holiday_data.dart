library;

/// 单年假期数据
class YearHolidayData {
  /// 法定假日日期 → 假日名称（key 格式：'month-day'，如 '1-1'）
  final Map<String, String> holidays;

  /// 调休补班日期（key 格式：'month-day'，如 '1-26'）
  final Set<String> workdays;

  const YearHolidayData({
    required this.holidays,
    required this.workdays,
  });

  /// 获取某天的假日名称，若无则返回 null
  String? getHoliday(DateTime date) {
    final key = _dateKey(date);
    return holidays[key];
  }

  /// 某天是否是调休补班日
  bool isWorkday(DateTime date) {
    final key = _dateKey(date);
    return workdays.contains(key);
  }

  String _dateKey(DateTime date) => '${date.month}-${date.day}';
}

